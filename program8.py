#ยกตัวอย่างเขียนแยกตัวเลขคู่หรือเลขคี่
number = int(input("กรุณาป้อนตัวเลขของคุณ:"))#รับตัวเลขผ่านkeybord
print("เลขของคุณ คือ ",number)

#เขียนแบบเงื่อนไขธรรมดา
if number%2==0:
    print("เลขคู่")
else:
    print("เลขคี่")

print("-------------")

#เขียนแบบลดรูป
print("เลขคู่") if number%2==0 else print("เลขคี่")
