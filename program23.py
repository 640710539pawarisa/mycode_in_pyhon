#สร้างlist เก็บกลุ่มของสี note ;liistเก็บข้อมูลซ้ำกันได้

color = ["แดง","เหลือง","เขียว"]#แบบ1
color2=list(("ขาว","ฟ้า","ส้ม")) #แบบ2
#รวมlist ในตัวแปรชื่อ fullcolors
fullcolors=color+color2
print(fullcolors,"รวมlist ในตัวแปรชื่อ fullcolors")
#เช็คชนิดข้อมูล
print(type(fullcolors),"ทดสอบเช็คชนิดข้อมูล")
#เช็คจํานวนข้อมูล
print(len(fullcolors),"ทดสอบเช็คจํานวนข้อมูล")

print("-------------------------------------------------------------------")

#เข้าถึงข้อมูลในlistแบบกำหนดช่วง

color3=["แดง","เหลือง","เขียว","ดํา","ขาว","ฟ้า","ส้ม"]

#ทำจากข้างหน้าไปข้างหลัง
print(color3[2],"เขียว")#อยากได้สีเขียว
#อยากได้สีดำกับขาว
print(color3[3:5],"ดํากับขาว")#การเข้าถึงข้อมูลในlistแบบกำหนดช่วง
#อยากได้สีดำกับขาวและส้ม
print(color3[3:6],"ดํากับขาวและส้ม")

#อยากได้ตั้งแต่เริ่มต้นถึงสีเขียว
print(color3[:3])#0-2
#ทำจากข้างหลังไปข้างหน้า
print(color3[-3],"ขาว")
print(color3[-6:-2],"ดํากับขาว")
print(color3[-6:],"ดํากับขาวและส้ม")
#อยากได้ตั้งแต่เริ่มต้นถึงตำแหน่งสุดท้าย
print(color3[:])
#อยากได้ตั้งแต่ตำแหน่งสุดท้าย
print(color3[-1:])

print("-------------------------------------------------------------------")

#เข้าถึงข้อมูลแบบกำหนดช่วงจุดเริ่มต้นกับจุดสิ้นสุด

color4=["แดง","เหลือง","เขียว","ดํา","ขาว","ฟ้า","ส้ม"]
#แดง = 0,เหลือง = 1,เขียว = 2,ดํา = 3,ขาว = 4,ฟ้า = 5,ส้ม = 6 แบบหน้าไปหลัง
#แดง = -7,เหลือง = -6,เขียว = -5,ดํา = -4,ขาว = -3,ฟ้า = -2,ส้ม = -1
#ทำจากข้างหน้าไปข้างหลัง
print(color4[2:5])#เขียว,ดํา,ขาว ,เราอยากได้ข้อมูลตรงกลาง
#ทำจากข้างหลังไปข้างหน้า
print(color4[-4:-1])#ดํา,ขาว,เราอยากได้ข้อมูลตรงกลาง
#อยากได้ดำ,ขาว,ฟ้า,ส้ม
print(color4[-4:])#ดํา,ขาว,ฟ้า,ส้ม
