#Listสามารถเปลี่ยนค่าได้ , สามารถเพิ่มค่าได้,เก็บกลุ่มข้อมูลได้

#เก็บข้อมูลสินค้า แบบที่ 1
product = ["กางเกง",99.99,10,True]

#เก็บข้อมูลสินค้า แบบที่ 2
product = list()#แบบไม่มีข้อมูล
product = list(("กางเกง",99.99,10,True))

#ตรวจสอบชนิดข้อมูล
print(type(product),"ทดสอบเช็คชนิดข้อมูล")

#ตรวจสอบว่ามีข้อมูลกี่ตัว
print(len(product),"ทดสอบเช็คจํานวนข้อมูล")

#การเข้าถึงข้อมูล แบบซ้ายไปขวา
print(product[0])#ข้อมูลตัวแรก ,มองจากข้างหน้าไปข้างหลัง

print(product[1])#ข้อมูลตัวที่ 2
print(product[2])#ข้อมูลตัวที่ 3
print(product[3])#ข้อมูลตัวที่ 4

#การเข้าถึงข้อมูล แบบขวาไปซ้าย
print(product[-1])#ข้อมูลตัวสุดท้าย ,มองจากข้างหลังไปข้างหน้า

print(product[-2])#ข้อมูลตัวที่ 2
print(product[-3])#ข้อมูลตัวที่ 3
print(product[-4])#ข้อมูลตัวที่ 4

print("-------------------------------------------------------------------")
#ถ้าอยากดึงข้อมูลทกตัว
for element in product: #elementคือชื่อตัวแปร
    print(element)  
#ถ้าอยากแทรกแก้ไขข้อมูล เช่นอยากเปลี่ยนข้อมุล จาก กางเกงเป็นเสื้อ

product[0] = "เสื้อ"
print(product,"แทรกแก้ไขข้อมูล")

product[1]=250
print(product,"แทรกแก้ไขข้อมูล ราคา")

print("-------------------------------------------------------------------")
