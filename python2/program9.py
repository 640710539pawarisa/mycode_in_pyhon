#สร้างฟังก์ชันแสดงตารางแม่สูตรคูณ

#แม่สูตรคูณแบบ1
def showtable(): #ชื่อฟังก์ชัน
    num=int(input("ป้อนตารางแม่สูตรคูณของคุณ: "))
    print("ตารางแม่สูตรคูณ แม่",num)
    print("------------------") #ห้ามปล่อยว่างเด็ดขาด มันจะ error ให้เราใส่ คำสั่ง pass ก้ได้
    for i in range(1,13): #range(start,stop) คือ 1-12
        print(num,"x",i,"=",num*i) #number*i คือ แม่สูตรคูณ และ i คือ หมายเลขรอบคูณกับตัวเลขที่เราป้อน
        #หรือเขียนเป็น
        # print(f"{num} x {i} = {num*i}") #แบบนี้ก็ได้
    print("------------------")
    
#แม่สูตรคูณแบบกำหนดช่วง2 
def showtable2(num2): #ชื่อฟังก์ชัน
    print("ตารางแม่สูตรคูณ แม่",num2)
    print("------------------") #ห้ามปล่อยว่างเด็ดขาด มันจะ error ให้เราใส่ คำสั่ง pass ก้ได้
    for i in range(1,13): #range(start,stop) คือ 1-12
        print(f"{num2} x {i} = {num2*i}") 
    print("------------------")
