#การ สร้าง Function แบบมี parameter

#เราจะลองสร้างฟังก์ชันเพื่อทำงานง่ายๆดูก่อนเช่น
#***สิ่งที่เราต้องการเพิ่ม เช่นเราเรียกใช้ฟังก์ชัน sayhello() เราอยากที่จะแสดงข้อความ ระบุบบุคคลที่เราอยากทักทายด้วย***
#และตารางสูตรเราอยากแสดงตารางมากกว่าแม่2

#เราจะทำได้ ผ่านการส่งข้อมูลโดยนิยามเป็น parameter ให้สร้างตัวแปรในพามิเตอร์ แล้วนําไปใช้ในฟังก์ชัน
def sayhello(time ,username , age): #parameter ชื่อ username เก็บชื่อผู้ใช้ และ age เก็บอายุ และ time เก็บเวลา
    print("สวัสดีค่ะ", time, username)#กระบวนการทำงาน
    print("ปีนี้คุณมีอายุคือ", age)
    
    
