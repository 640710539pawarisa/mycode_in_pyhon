#สร้างฟังก์ชันสำหรับให้บริการบันทึกข้อมูลของพนักงานลงในระบบ
def saveemployee(name,department,salary): #ชื่อฟังก์ชัน saveemployee คือ ฟังก์ชันสําหรับบันทึกข้อมูลพนักงาน , name เก็บชื่อพนักงาน 
    #, department เก็บแผนก , salary เก็บเงินเดือน
    print(f"บันทึกข้อมูลพนักงาน {name} แผนก {department}")
    print(f"เงินเดือนของพนักงาน {name} คือ {salary} บาท")
    print("----------------------------------")
