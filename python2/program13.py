#ลองทำฟังก์ชันที่ตรวจสอบข้อมูลตัวเลข โดยจะให้ส่งข้อมูลตัวเลขเข้ามาทำงานในฟังก์ชัน แล้วให้ตวจสอบว่า เป็นเลขคู่หรือเลขคี่ แล้วส่งค่ากลับออกมา

def checknumber(number): #ชื่อฟังก์ชัน
    if number%2==0: #เช็คว่าเป็นเลขคู่หรือเลขคี่
        return "เลขคู่"
    else: #ถ้าเป็นเลขคี่
        return "เลขคี่"
    
#เรียกใช้ฟังก์ชัน
#สร้างตัวแปรมาเก็บผลลัพธ์
reslt = checknumber(10)#เลข 10 คือ ที่เราจะส่งเข้ามาในฟังก์ชันchecknumber แล้วเก็บไว้ในตัวแปร reslt
print(reslt)

