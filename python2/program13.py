#Funtion ที่เอาไว้คำนวณผลรวมของตัวเลข
def summation(*args): #สร้างฟังก์ชันชื่อ summation ที่รับค่ามาเป็นจํานวนไม่จํากัด ,*args คือตัวแปรส่งแบบลำดับ
    total = 0 #สร้างตัวแปร total เพื่อเก็บผลรวม
    for item in args:
        total += item #คือ total = total + item
    return total
    
#เรียกใช้ฟังก์ชัน
print(summation(10,20))
print(summation(10,20,30))
#ถ้าอยากนำเอาผลไปใช้ส่วนอื่น แนะนำว่าให้สร้างตัวแปรมาเก็บเหมือนในกรณีของ result = checknumber(10) print(result)
